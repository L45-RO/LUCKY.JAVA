package javaprograms;

public class Reverse_Array {
	public static void main(String[] args) {
		int[]arr=new int[10];
		arr[0]=1;
		arr[1]=10;
		arr[2]=17;
		arr[3]=15;
		arr[4]=13;
		arr[4]=13;
		arr[6]=135;
		arr[7]=1353;
		arr[8]=1865;
		arr[9]=14357874;
		for(int i=arr.length-1;i>=0;i--) {
			System.out.println(arr[i]);
		}
	}


}
